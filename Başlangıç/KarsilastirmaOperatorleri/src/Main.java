public class Main {
    public static void main(String[] args) {
        /*

        Karşılaştırma Operatörleri
        == ---> Eşit mi?
        != ---> Eşit değil mi?
        > ---> Büyük mü?
        >= ---> Büyük ve ya Eşit mi?
        < ---> Küçük mü?
        <= ---> Küçük ve ya Eşit mi?

         */

        System.out.println(3==4);
        System.out.println(3!=3);
        System.out.println(2<3);
        System.out.println(2<=3);
        System.out.println(3>3);
        System.out.println(3>=3);
        System.out.println("SEMIH"=="SEMIH");
        System.out.println("SEMIH">"SEMIH"); // iki string karşılaştıralamayacağı için hata veriyor

    }
}
