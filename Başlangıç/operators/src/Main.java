public class Main {
    public static void main(String[] args) {
        int a = 4;
        a += 2; //a = a + 2;
        a *= 4;
        System.out.println(a);
        System.out.println("--------------------");
        int b = 7;
        int c = 6;
        b++; //++b
        c--; //--c
        System.out.println(b);
        System.out.println(c);
        System.out.println("--------------------");
        System.out.println(+b);
        System.out.println(--c);
        System.out.println("--------------------");
        System.out.println((3f / 4) + 4 * 5);

    }


}
